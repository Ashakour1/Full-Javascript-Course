// COMPARISON OPERATOR;

// == Equal
// != Not Equal
// === Strict Equal
// !== Not Strict Equal
// > greater than
// >= greater than or Equal
// < less than
// <= less than or Equal

let num1 = 10;

let num2 = 1;

// == Equal
console.log(num1 == num2); 
// the output is false


// != Not Equal
console.log(num1 != num2);
// the output is true

// === Strict Equal
console.log(num1 === num2);
// the output is false

// !== Not Strict Equal
console.log(num1 !== num2);
// the output is true

// > greater than
console.log(num1 > num2);
// the output is true

// >= greater than or Equal
console.log(num1 >= num2);
// the output is true

// < less than 
console.log(num1 < num2);
// the output is false

// <= less than or Equal
console.log(num1 <= num2);
// the output is false



